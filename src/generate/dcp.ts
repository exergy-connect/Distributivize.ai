function recommendedTaskSize(totalItems: number): number {
  if (totalItems <= 100) return Math.max(1, totalItems);
  if (totalItems <= 10_000) return 250;
  return 1_000;
}

function chunk<T>(items: T[], size: number): T[][] {
  const chunks: T[][] = [];
  for (let index = 0; index < items.length; index += size) {
    chunks.push(items.slice(index, index + size));
  }
  return chunks;
}

