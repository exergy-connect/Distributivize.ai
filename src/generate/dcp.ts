import { promises as fs } from "node:fs";
import path from "node:path";
import { DcpCandidate, ProjectReport } from "../core/types.js";
import { ensureDirectory } from "../core/fs.js";

export async function generateDcpScaffold(report: ProjectReport): Promise<string[]> {
  const outputDirectory = path.join(report.projectRoot, ".distributivize", "wrappers");
  await ensureDirectory(outputDirectory);
  await cleanGeneratedDirectory(outputDirectory);

  const generated: string[] = [];
  const suitable = report.candidates.filter((candidate) => candidate.score >= 50);

  const indexPath = path.join(outputDirectory, "index.ts");
  await fs.writeFile(indexPath, buildIndex(suitable), "utf8");
  generated.push(indexPath);

  for (const candidate of suitable.slice(0, 12)) {
    const fileName = `${slug(candidate.id)}.dcp.ts`;
    const filePath = path.join(outputDirectory, fileName);
    await fs.writeFile(filePath, buildWrapper(candidate), "utf8");
    generated.push(filePath);
  }

  return generated;
}

async function cleanGeneratedDirectory(outputDirectory: string): Promise<void> {
  const entries = await fs.readdir(outputDirectory, { withFileTypes: true });
  await Promise.all(
    entries
      .filter((entry) => entry.isFile() && (entry.name.endsWith(".dcp.ts") || entry.name === "index.ts"))
      .map((entry) => fs.unlink(path.join(outputDirectory, entry.name)))
  );
}

function buildIndex(candidates: DcpCandidate[]): string {
  const exports = candidates
    .slice(0, 12)
    .map((candidate) => `export * from "./${slug(candidate.id)}.dcp.js";`)
    .join("\n");

  return `// Generated by distributivize. Review before running in production.
${exports || "// No medium/high suitability candidates were found."}
`;
}

